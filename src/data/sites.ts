export function getSitesByCategory(category: SiteCategory | 'all'): Site[] {
  if (category === 'all') return sites;
  return sites.filter((site) => site.category === category);
}

export function searchSites(query: string): Site[] {
  const lower = query.toLowerCase();
  return sites.filter(
    (site) =>
      site.name.toLowerCase().includes(lower) ||
      site.description.toLowerCase().includes(lower) ||
      site.tags.some((tag) => tag.toLowerCase().includes(lower))
  );
}
